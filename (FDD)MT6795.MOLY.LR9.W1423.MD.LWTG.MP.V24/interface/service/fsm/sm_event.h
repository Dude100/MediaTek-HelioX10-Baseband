/* TYPE DEFINITIONS ***************************************************************/

typedef struct EventList
{
   struct EventListObj *first ;
   struct EventListObj *last ;
   struct EventListObj *chaser ;
   kal_uint32 dirtyCount;
   struct EventListObj *lookup;
} EventList ;

typedef struct EventHolderTag
{
   kal_uint32 eventId ;    //event identifier
   kal_uint32 processId ;  //process number
   kal_uint32 instanceId ; //instance number
   ilm_struct *pMsg ;      //message associated with the event
} tEventHolder ;

typedef struct EventListObj
{
	tEventHolder         *event ;
	struct EventListObj  *prev ;
	struct EventListObj  *next ;
	unsigned int         dirtyCount ;
} EventListObj;

